# Replace Method
my_string = "Hellow Python"
print(my_string.replace("Hellow", "The"))

# Reassign Variable
name = "John Doe"
name1 = name
print(name1)
print(name)

# String Indexing
my_text = "Hello Pythonic"
print(my_text[0])
print(my_text[6:14])
# Reverse Method
print(my_text[::-1])

# Sring in Multiple Times
my_text = "Python "
number = 5
print(my_text * number)

# Count Method
my_text = "Python Programing Problem"
print(my_text.count("P"))

